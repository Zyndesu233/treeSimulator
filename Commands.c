void runHelp() {
    char* subcommand = strtok(NULL, " \n");
    if (!subcommand) {
        printf("[INFO] Printing command list:\n");
        for (int i = 0; i < COMMAND_NUMBER; i++) {
            printf("%2d. ", i + 1);
            printCommandFormat((Command)i);
        }
        printf("\n");
        printf("Use \"help <command>\" command to get more detail.\n");
        return;
    }

    printWarning("Some mannuals is generated by AI");
    char filePath[] = "Manuls/";
    strcat(filePath, reverseHashCommand(hashCommand(subcommand)));
    strcat(filePath, ".txt");
    FILE* manul = fopen(filePath, "r");
    if (!manul) {
        printError("Manual not found.");
        return;
    }
    char buffer[1000];
    while (fgets(buffer, 1000, manul)) {
        printf("%s", buffer);
    }
    fclose(manul);
    printf("\n");
}

